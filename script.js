function toggleFaq(index) {
    const targetItem = document.getElementById(`faq-item-${index}`);
    const isActive = targetItem.classList.contains('active');
    
    // Optional: Close others for a clean accordion look
    document.querySelectorAll('.faq-item').forEach(item => {
        item.classList.remove('active');
        item.querySelector('.faq-question').setAttribute('aria-expanded', 'false');
    });
    
    // Toggle current
    if (!isActive) {
        targetItem.classList.add('active');
        targetItem.querySelector('.faq-question').setAttribute('aria-expanded', 'true');
    }
}


