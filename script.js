window.onscroll = function() {
    const backToTop = document.getElementById('backToTop');
    const hub = document.getElementById('ticketHub');
    const isMobile = window.innerWidth <= 768;
    const scrollTop = window.pageYOffset || document.documentElement.scrollTop;

    // Show/Hide Back to Top button based on distance from top
    if (scrollTop > 300) {
        backToTop.classList.add('visible');
    } else {
        backToTop.classList.remove('visible');
    }

    // Manual Scroll Resurrection (User scrolls up without clicking the button)
    if (scrollTop < 50 && hub && hub.style.display === 'none') {
        hub.style.display = 'flex';
        setTimeout(() => {
            hub.style.opacity = "1";
            hub.style.transform = "translateY(0) scale(1)";
            if (backToTop) backToTop.style.bottom = isMobile ? "90px" : "110px";
        }, 50);
    }
};

